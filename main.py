def on_button_pressed_a():
    MuseOLED.clear()
    MuseOLED.write_string_new_line("Temp: ")
    MuseOLED.write_string(convert_to_text(input.temperature()))
    basic.pause(5000)
input.on_button_pressed(Button.A, on_button_pressed_a)

